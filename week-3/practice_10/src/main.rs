fn main() {
    // addition
    let sum:f64 = 5550.00 + 7310.00;
    println!("The sum of 5550.00 and 7310.00 is {}",sum);

    
    // subtraction 
    let difference:f32 = 95.5 - 4.3;
    println!("The difference of 95.5 and 4.3 is {}",difference);

    // multiplication
    let product:f32 = 4.00 * 30.00; 
    println!("The product of 4.00 and 30.00 is {}",product);

   // division
   let quotient:f32 = 56.7 / 32.2;
   println!("The quotient of 56.7 and 32.2 is {}",quotient);

   // remainder 
   let remainder:f32 = 43.0 % 5.0;
   println!("The remainder of 43.0 and 5.0 is {}",remainder); 
}


